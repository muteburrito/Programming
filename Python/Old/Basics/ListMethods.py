# Similar to string methods we can use many different methods for lists 
# To access methods of a list we use dot

l1 = [1,4,2,6,0,19,10]
print(l1)
l1.sort() # This will sort the original list in ascending order
print(l1)
l1.reverse() # This will reverse
print(l1)
l1.append(45) # This wil add to the end of list
print(l1)
l1.insert(3,8) # This means 8 will be added in the index 3
print(l1)
l1.pop(3) # This will remove the value at that index 
print(l1)
l1.remove(45) # This will remove the number or data from the list
print(l1)
