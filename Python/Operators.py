b = 5
#Arithmetic Operator
print("The value of a + b = ", a + b)
print("The value of a - b = ", a - b)
print("The value of a * b = ", a * b)
print("The value of a / b = ", a / b)

#Assignment Operator
c = 3 # '=' is called assignemnt operator
print(c)
c += 3 # Here 3 will be added and then c will be printed
print(c)
c -= 3
print(c)
c *= 3
print(c)
c /= 3
print(c)

#Comparison Operator
print(a==b) #this will return a boolean value
print(a<b)
print(a>b)
print(a<=b)
print(a>=b)
print(a!=b) #This is not equal operator

#Logical Operator
print((a>b) and (a<b)) #this will give true if both have value true
print((a>b) or (a<b)) #this will give true if either of one is true
print(not(a==b)) #this will give opposite value of original 