public class variables {
    public static void main(String[] args) {
        int a = 5; //4 bytes -> 32 bits
        /* Here by applying int we are declaring the a variable as integer */

        String str = "Hello this is a string";

        /*In Java we define string with String strictly case sensitive. */

        long l = 5000000; //8 bytes

        short s = 2; //2 bytes 16 bits range = -32768 to 32767

        byte b = 3; //1 byte -> 8 bits range = -128 to 127

        float f = 5.5f;

        double d = 5.5;

        char c = 'a';
        System.out.print(a);

        System.out.print(s);
    }
}
